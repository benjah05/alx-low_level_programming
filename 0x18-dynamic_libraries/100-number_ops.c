#include "operators.h"
/**
 * add - add 2 numbers
 * @x: first number
 * @y: second number
 * Return: the sum of x and y
 */
int add(int x, int y)
{
	return (x + y);
}
/**
 * sub - subtract 2 numbers
 * @x: first number
 * @y: second number
 * Return: the difference between x and y
 */
int sub(int x, int y)
{
	return (x - y);
}
/**
 * mul - multiply 2 numbers
 * @x: first number
 * @y: second number
 * Return: the product of x and y
 */
int mul(int x, int y)
{
	return (x * y);
}
/**
 * div - divide 2 numbers
 * @x: first number
 * @y: second number
 * Return: the quotient of x and y
 */
int div(int x, int y)
{
	return (x / y);
}
/**
 * mod - modulus/ remainder of 2 numbers
 * @x: first number
 * @y: second number
 * Return: the remainder of x / y
 */
int mod(int x, int y)
{
	return (x % y);
}
